public class DoubleItems {
  public static void main(String[] args) {

    int[] ag = new int[5];

    ag[0] = 3;
    ag[1] = 4;
    ag[2] = 5;
    ag[3] = 6;
    ag[4] = 7;

    for (int i = 0; i < ag.length; i++) {
      System.out.println(ag[i] * 2);
    }
  }
}

